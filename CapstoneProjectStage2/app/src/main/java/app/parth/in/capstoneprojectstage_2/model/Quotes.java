package app.parth.in.capstoneprojectstage_2.model;

public class Quotes {
    String name;
    String author;

    public Quotes(String name, String author) {
        this.name = name;
        this.author = author;
    }

    public String getName() {
        return name;
    }

    public String getAuthor() {
        return author;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setAuthor(String author) {
        this.author = author;
    }
}
